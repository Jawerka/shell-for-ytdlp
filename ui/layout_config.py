def get_card_kwargs():
    """Параметры для карточек."""
    return {
        "fg_color": BG_CARD,
        "corner_radius": CORNER_RADIUS,
        "border_width": 0,
    }


def get_button_kwargs():
    """Параметры для кнопок."""
    return {
        "width": BTN_SIZE_W,
        "height": BTN_SIZE_H,
        "corner_radius": CORNER_RADIUS,
        "text_color": TEXT_PRIMARY,
        "font": ("", BTN_FONT_SIZE),
    }


def get_log_viewer_kwargs():
    """Параметры для логгера."""
    return {
        "font": (LOG_FONT_FAMILY, LOG_FONT_SIZE),
        "corner_radius": CORNER_RADIUS,
        "padx": ELEMENT_PADX,
        "pady": ELEMENT_PADY,
    }


def get_progress_bar_kwargs():
    """Параметры для прогресс-бара."""
    return {
        "bar_height": PROGRESS_HEIGHT,
        "font_size": PROGRESS_FONT_SIZE,
        "corner_radius": CORNER_RADIUS,
        # padx не указан — прогресс-бар на всю ширину
    }
