# ============================================================================
# СИСТЕМА ОТСТУПОВ
# ============================================================================
class Spacing:
    """Система отступов приложения (кратна 4px)."""

    XS = 4
    SM = 8
    MD = 12
    LG = 16
    XL = 24
    XXL = 32

    # Готовые комбинации
    CARD_PADDING = (MD, MD)
    PAGE_PADDING = (XL, XL)
    SECTION_GAP = (0, LG)
    BUTTON_GAP = (SM, SM)
    INPUT_HEIGHT = 42

# ============================================================================
# ФУНКЦИИ ТЕМЫ
# ============================================================================
def setup_theme() -> None:
    """Инициализация темы customtkinter."""
    ctk.set_appearance_mode("dark")
    # Не обязательно менять стандартную тему цвета в CTk, но оставим нейтральную базовую
    try:
        ctk.set_default_color_theme("dark-blue")
    except Exception:
        # fallback: если тема не найдена, ничего не падает
        pass


def get_color(name: str) -> str:
    """Получить цвет по имени из темы."""
    return COLOR_THEME.get(name, "#000000")


def get_radius(size: str = "md") -> int:
    """Получить радиус скругления по размеру."""
    return COLOR_THEME.get(f"radius_{size}", 8)


def get_spacing(name: str) -> int:
    """Получить отступ по имени."""
    return getattr(Spacing, name, Spacing.MD)
